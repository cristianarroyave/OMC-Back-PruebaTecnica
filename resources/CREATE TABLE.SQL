create schema MODELOTODOS;

create type MODELOTODOS.DIRECCION as (
	CALLE VARCHAR,
	CIUDAD VARCHAR,
	CODIGO_POSTAL VARCHAR,
	PAIS VARCHAR
)

CREATE TABLE MODELOTODOS.USUARIOS (
	ID SERIAL,
	NOMBRE VARCHAR(20) not null,
	USERNAME VARCHAR(20) not null,
	PASSWORD VARCHAR(20) not null,
	ADDRESS VARCHAR(50),
	CONSTRAINT USUARIOS_PK PRIMARY KEY (ID)
);

CREATE TABLE MODELOTODOS.TODOS (
	ID SERIAL,
	TITULO VARCHAR(30) not NULL,
	COMPLETADO BOOLEAN not NULL,
	USUARIO INTEGER not null,
	CONSTRAINT TODOS_PK PRIMARY KEY (ID),
	CONSTRAINT USUARIOS_TODO_FK FOREIGN KEY (USUARIO) REFERENCES MODELOTODOS.USUARIOS(ID)
);